package com.innouni.yinongbao.unit.exper;

import java.util.List;

/***
 * 个人文章实体类
 * @author LinYuLing
 * @UpdateDate 2014-09-25
 */
public class ExperTechnologyUnit {
	/***
	 * id
	 */
	private String id;
	/***
	 * 标题
	 */
	private String title;
	/***
	 * 发布时间
	 */
	private String addtime;
	/***
	 * 详细内容
	 */
	private String message;
	/***
	 * 浏览量
	 */
	private String viewnum;
	/***
	 * 回复量
	 */
	private String replynum;
	/***
	 * 评论列表
	 */
	private List<ExperTechnologyCommentUnit> comments;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getAddtime() {
		return addtime;
	}

	public void setAddtime(String addtime) {
		this.addtime = addtime;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public String getViewnum() {
		return viewnum;
	}

	public void setViewnum(String viewnum) {
		this.viewnum = viewnum;
	}

	public String getReplynum() {
		return replynum;
	}

	public void setReplynum(String replynum) {
		this.replynum = replynum;
	}

	public List<ExperTechnologyCommentUnit> getComments() {
		return comments;
	}

	public void setComments(List<ExperTechnologyCommentUnit> comments) {
		this.comments = comments;
	}

}
